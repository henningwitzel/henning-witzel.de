          <h3><a href="#creativity">Creativity, Inc</a></h3>
          <h5 class="grey bottom-small">Book by by Amy Wallace and Edwin Catmull · 2014</h5>
          <p>I’m a big fan of Pixar, and I’ve always wondered how they been able to create so many fantastic movies over the years? Movies like Toy Story, Finding Nemo, WALL-E, Cars, Monsters Inc., Ratatouille. In this book, Ed answers the question, and you can learn about their growth and how they found a way to manage creativity. For me, it’s a must-read for product designers, managers, and artists that want to invent and create new products.</p>
          <p class="bottom-large"><i>What stuck with me</i> is the concept of the Braintrust. To achieve a certain level of honesty, they created a group of trusted colleagues that review the progress of a film that is in development. It’s an environment where honesty is possible; directors and creative teams can give and receive direct and constructive feedback to each other. According to Ed, the job of the Braintrust is to <i>“push towards excellence, and root out mediocrity.”</i> If I made you curious here two articles that go more into detail. The first one talks about <a href="https://medium.com/great-business-stories/lessons-from-pixar-1-the-braintrust-e306843a5153" target="_blank">lessons you can learn </a> from the Braintrust and second one about <a href="https://blog.prototypr.io/pixars-braintrust-why-it-is-important-for-a-creative-culture-7dcbee3aa8f5" target="_blank">why it’s important</a> for a creative culture.</p>
