      <h1>Mobile POS for Store Associates</h1>      
      <h4 class="grey bottom-large">An app that makes it easy for store associates to serve customers.</h4>
      <p class="in-progress bottom-large">üöß This site is still work in progress and there will be regular updates in the next weeks üöß</br><i>Last update on July 5th, 2020</i></p>
      <p>By mid of 2017, we decided to build a completely new version of our mobile POS app that allows our customers to run their store on iPhones. With a team of 12 front-end developers, multiple back-end teams, and a product manager on my side, we launched the first version in February 2018. This version came with a set of functionality that fulfilled the basic needs to operate a store.</p>
      
      <p>Since then, I continuously try to improve the user experience, do regular research, challenge my designs, analyze user flows, and work together with various product teams that want to add more functionality. The challenge we are phasing is to <b>keep its simplicity while more features are demanded</b> by our customers and to move up market.</p> 
      
      <p>Below you can find four problems I worked on where good user experience was key to success. In order to come up with good solutions I describe some of the research methods we have used to learn and validate our assumptions.</p>

