      <h3 id="research" class="top-large bottom-small">User Research</h3>
      <p>In the following part, I describe some of the mechanisms we use since we launched the app to gather user research. For a B2B company like NewStore that builds retail software but does not operate a retail brand, the research is critical to be successful.</p>

      <p><i>How do we know how good our software is? How well did we solve the problems in the store? What kind of challenges they face every day? What workarounds are in place that we could simply make go away?</i></p>

      <p>We set ourselves up to challenge the old assumptions of existing retail technology and antiquated in-store practices. Our goal is to <b>provide an intuitive experience for retailers and customers that brings joy back to the shopping experience</b>.</p>

      <p>Not an easy thing todo and a balance act with a certain risk attached of falling into the <a href="https://playbook.uie.com/blog/experience-rot">Experience Rot</a> and ending up with a complex system nobody wants to use.</p>
      <p>...</p>
      <p><b>Working as a store associate</b> ‚Äì Eat your own dog food and use your product. We put designers and programmers into retail stores and let them be an associate for a day. It's an incredibly powerful way to build empathy and get to know the feeling of being with a customer and depending on the app to work. </p>
      <p><b>Observation</b> ‚Äì See and learn how the user is using your product. Besides all the interviews and testing, seeing the app in action gives you a good sense of how well you solved the problems. </p>
