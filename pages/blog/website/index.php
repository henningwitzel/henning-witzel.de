      <h1>My new (old) website</h1>
      <h4 class="grey bottom-large">February 2nd, 2020 · 3:50 min read</h4>      
      <p>Actually, this should have been the first article in my blog explaining what happened to my website. In the last five years, I had many attempts for a redesign or even getting rid of it but never found the motivation – that changed at the end of last year.</p>

      <p>Since 2008 I have that domain, and it was always full of pictures.  For many years photography was my hobby, and the website was an easy way of showing my photos. It also helped me to make some money on the side by attracting new customers, in my case, couples who wanted to get married.</p>

      <p>The last time I updated the site was 2014. After I moved to Berlin, a lot of things changed in my life but not the website. For years I didn't look at it anymore until someone mentioned it in November last year.</p>

      <h3 class="top-large bottom-small">It was entirely in German</h3>

      <p>I was looking for a new UX designer to extend my design team. One part of the hiring process is to scan portfolios people send me. A lot of people attach a PDF, but some have their own websites. Every time someone referred to his site, I caught myself in beeing way more engaged and interested in the person. And then it happened, one UX designer I was interviewing mentioned my website. He spoke very fondly about it even it was entirely in German. I felt terrible and told him straight this is all old stuff, and I haven't redone it for many years. This made me realize it's time for a change – I need to get rid of all that old stuff and think about something new.</p>

      <p>In December, I decided to give it a go, but this time with a different focus – <b>content first, design second</b>. In the past, it was always the other way. I had a new design but didn't know what to put in.</p>

      <p>I downloaded the site from my webserver, trying to get it running locally to remove all the old pages and photos. Sounded easier then I thought. I spent more than an hour figuring out why I'm not able to open the subpages. I copied all the files from the server, compared them, and everything looked the same. <i>Why doesn't it work? Ha! Don't always trust your eyes; there is one more file</i>. A hidden file that allows navigating to other folders called "<i>.htaccess</i>" – those types of files are usually not shown in the Finder on Mac OSX. Finally, it worked!</p>

      <h3 class="top-extra-large bottom-small">Many things are getting thrown away</h3>

      <p>Having your own website started to feel like a lot of fun again. Nobody tells you what to do or whatnot. You can freely design and develop it the way you want to. The current design and framework behind are self-made. Back in 2012, my friend <a href="https://www.linkedin.com/in/stevenluettig/" target="_blank">Steven</a> and I build it together. While I focused on the design, he did most of the development work. I'm proud it's still in use since so many things are getting quickly thrown away.</p> 

      <p>The Internet became a place where companies and their algorithms control a lot of information. You need to hope that the post you saw on Instagram or Facebook will pop up again. Or a new article at Medium you want to read is not accessible because you reached your limit for the month.</p>

      <p>My reloaded side is an attempt for an independent place of all my thoughts and projects. I want to become a better writer, present work I have done, and help others to grow from the things I learned. You can read my articles. See what kind of books or podcasts I like. Take a look at specific projects I worked on and, of course, learn something about me — no limitations, no algorithms, no advertisements, just a simple website.</p>

      <h3 class="top-extra-large bottom-small">Let's start</h3>

      <p>To allow myself adding new content, I needed to define the goal of each page. What do I want to say? What does the visitor should do or get out of it? I began with the <a href="###basepath###/books" target="_blank">Books</a> page</p>
      
      <p>I put a list together of some of my favorites I have read in the past years. Every book has a description where I set myself the goal to write two short paragraphs. Something general about the book and <i>What stuck with me</i>: the ladder is what I usually tell people if I recommend them the book. I'm still in the process of giving everyone the second part. It's quite hard to remember because some of them I haven't read in years.</p></p>

      <p class="bottom-large">I loved playing around with CSS. While I was doing the design directly in HTML, I gave each book cover a little shadow, rounded corners, and an animation – give it a try and hover over a book. A relique from my past is the big header image. It has parallax scrolling and resizes itself depending on the screen. First, I wanted to get rid of it, but I changed my mind <strike>I was trying to remove it, failed, and got frustrated</strike>. </p>
      
      <a class="image-link" href="###basepath###pages/blog/website/images/headers.png"><img src="###basepath###pages/blog/website/images/headers.png" /></a>    

      <p class="top-large">In my heart, I'm still a photographer, and every time I go on vacation, I have my camera with me. I looked through my photos and picked pictures from my two-months sabbatical last year with the idea that the image somehow relates to the content.</p>      
    
      <p>Each book has a link where you can find more information about it. I decided to go against affiliate links. I'm not interested in making any money out of that. If you like a book and want to buy it, then consider going to an independent bookstore in your city. Why should you do that? Well, here is a podcast I listened to that opened my eyes – <a href="https://rework.fm/nevermore-amazon/" target="_blank">Nevermore, Amazon</a>.
      
