      <h1>My new (old) website</h1>
      <h4 class="grey bottom-large">January 12th, 2020 · 2:30 min read</h4>      
      <p>Actually, this should have been the first article in my blog explaining what happened to my website. In the last five years, I had many attempts for a redesign or even getting rid of it but never found the motivation – that changed at the end of last year.</p>

      <p>Since 2008 I have that domain, and it was always full of pictures.  For many years photography was my hobby, and the website was an easy way of showing my photos. It also helped me to make some money on the side by attracting new customers, in my case, couples who wanted to get married.</p>

      <p>The last time I updated the site was 2014. After I moved to Berlin, a lot of things changed in my life but not the website. For years I didn't look at it anymore until someone mentioned it in November last year.</p>

      <h3 class="top-large bottom-small">It was entirely in German</h3>

      <p>I was looking for a new UX designer to extend my design team. One part of the hiring process is to scan portfolios people send me. A lot of people attach a PDF, but some have their own websites. Every time someone referred to his site, I caught myself in beeing way more engaged and interested in the person. And then it happened, one UX designer I was interviewing mentioned my website. He spoke very fondly about it even it was entirely in German. I felt terrible and told him straight this is all old stuff, and I haven't redone it for many years. This made me realize it's time for a change – I need to get rid of all that old stuff and think about something new.</p>

      <p>In December, I decided to give it a go, but this time with a different focus – <b>content first, design second</b>. In the past, it was always the other way. I had a new design but didn't know what to put in.</p>

      <p>I downloaded the site from my webserver, trying to get it running locally to remove all the old pages and photos. Sounded easier then I thought. I spent more than an hour figuring out why I'm not able to open the subpages. I copied all the files from the server, compared them and everything looked the same. <i>Why doesn't it work? Ha! Don't always trust your eyes; there is one more file</i>. A hidden file that allows navigating to other folders called "<i>.htaccess</i>" – those types of files are usually not shown in the finder on Mac OSX. Finally, it worked!</p>

      <h3 class="top-extra-large bottom-small">Many things are getting thrown away quickly</h3>

      <p>Having your own website started to feel like a lot of fun again. Nobody tells you what to do or whatnot. You can freely design and develop it the way you want to. The current design and framework behind are self-made. I built it together with my friend Steven back in 2012, and I'm proud it's still in use. So many things are getting thrown away quickly.</p> 

      <p>The Internet became a place where companies and their algorithms control a lot of information. You need to hope that the post you saw on Instagram or Facebook will pop up again. Or a new article at Medium you want to read is not accessible because you reached your limit for the month.</p>

      <p>My reloaded side is an attempt for an independent place of all my thoughts and projects. I want to become a better writer, present work I have done, and help others to grow from the things I learned. You can read my <a href="###basepath###blog">articles</a>. See what kind of <a href="###basepath###books">books</a> or <a href="###basepath###podcasts">podcasts</a> I like. Take a look at specific projects I worked on and, of course, learn something <a href="###basepath###about_me">about me</a> — no limitations, no algorithms, no advertisments, just a simple website.</p>


