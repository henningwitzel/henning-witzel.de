      <h1>Productivity at Work</h1>
      <h4 class="grey bottom-large">January 3rd, 2020 · 2 min read</h4>      
      <p>When I joined <a href="###basepath###/about_me#newstore">NewStore five years ago</a>, I had no idea what it means to have a calendar that feels like a school timetable or an email inbox with 200 unread mails overnight. Real-time chat tools like Slack made it possible for everybody to reach out to me and interrupt whenever they think it's necessary. In the beginning, I thought I need to be responsive to everybody to not seem rude or unproductive – but actually, I started to feel unproductive.</p>

      <p>You go into the office in the morning and join one meeting after another. During the breaks or even during the meetings, you are checking Slack. And of course, your email inbox pops up every now and then to inform you about something that happened in Jira.</p>

      <p>It was time for a change! Certain books like <a href="###basepath###/books#crazy">It Doesn't Have to Be Crazy at Work</a> and Ted Talks like <a href="https://www.ted.com/talks/jason_fried_why_work_doesn_t_happen_at_work?language=en" target="_blank">the one below from Jason Fried</a> inspired me finding some solutions to deal with that.</p>

        <div style="max-width:854px"><div style="position:relative;height:0;padding-bottom:56.25%"><iframe src="https://embed.ted.com/talks/lang/en/jason_fried_why_work_doesn_t_happen_at_work" width="854" height="480" style="position:absolute;left:0;top:0;width:100%;height:100%" frameborder="0" scrolling="no" allowfullscreen></iframe></div></div>

      <h3 class="top-extra-large bottom-small">Regain control of your calendar</h3>
<p>Finding a time slot in someone's calendar is easy. I can just drag and drop in a meeting and book their time. <i>Did I consider that this person maybe wants to get some work done?</i> No, not at all. <i>Should I invite Andy as well, since he might know something about it?</i> Yeah, let's check his calendar. It was never easier to schedule something - thanks, Google!</p>

<p>This easiness comes with a cost: you're not in control anymore of your own time. I wanted to change that and started an experiment.</p>

<p>First, I tried to reduce the number of meetings I was invited to. <i>Do I really need to be there? The last three times, was there something important I would have otherwise missed? What was the purpose of this meeting again?</i>  I recommended communicating the outcome asynchronously via email so that I can read it in my own time.</p>

<p>Second I planned my calendar a couple weeks ahead and added working blocks. For example, if there was nothing on my calendar on a Monday between 10:00am and 12:30, I blocked 2,5 hours. After lunch until the first meeting I really need to go to, I can have another 3,5 hours of blocked time.</p>

<a class="image-link" href="###basepath###pages/blog/productivity/images/calendar.png"><img src="###basepath###pages/blog/productivity/images/calendar.png" /></a>

<p>After doing this for some weeks, it turned out that a lot of people told me they can't talk to me anymore since my calendar is always full. Well, sure you can speak to me! If you want to have a meeting with me, just ask upfront. At the same time, you can also let me know what's the purpose, and I can respond if a meeting is even necessary.</p>

<p>There were are still people who ignored the working blocks. Every time some did that without a description or agenda, I declined politely and asked them why do they want to meet. Some colleagues became more aware and didn't put a meeting in the middle of the 4-hour block and instead tried the beginning or the end – I appreciated that!</p>

<p>I finally had more time to focus on getting work done.</p>

