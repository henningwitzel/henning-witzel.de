      <p>For many years I've been using <a href="https://www.cockos.com/licecap/" target="_blank">LICEcap</a>, a simple tool to make animated screen captures for OS X and Windows. For my needs, it does what it is supposed to do. You can capture any area of your desktop and save it directly as a GIF file. That's it, nothing more, nothing less, and it comes for free.</p>

      <p>To record, you simply position and adjust the frame to what you want to capture on the screen. In the settings, you can control the frames per second and show mouse clicks if you wish. They just recently provided an update to make it work on the latest Mac OSX version.</p>

